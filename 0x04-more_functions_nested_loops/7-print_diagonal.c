#include "main.h"

/**
  * print-diagonal - diagonal in termina
  * @n: number of time \ will be printed
  *
  * Description: 'Print or draw lines on terminal'
  */

void print_diagonal(int n)
{
	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		int i, j;

		for (i = 0; i < n; i++)
		{
			for (j = 0; j < n; j++)
			{
				if (j == i)
					_putchar('\\');
				else if (j < i)
					_putchar(' ');
			}
			_putchar('\n');
		}
	}
}
